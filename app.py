def predict_insurance(age, sex, bmi, children, smoker, region):
    try:
        sex_encoded = 1 if sex == "Masculino" else 0
        smoker_encoded = 1 if smoker == "S√≠" else 0

        input_data = pd.DataFrame({
            'age': [age],
            'sex': [sex_encoded],
            'bmi': [bmi],
            'children': [children],
            'smoker': [smoker_encoded],
            'region_northwest': [1 if region == "Northwest" else 0],
            'region_southeast': [1 if region == "Southeast" else 0],
            'region_southwest': [1 if region == "Southwest" else 0]
        })

        prediction = insurance_model_final.predict(input_data)[0]

        if prediction < 5000:
            categoria = "üíö Bajo"
        elif prediction < 15000:
            categoria = "üíõ Moderado"
        else:
            categoria = "üî¥ Alto"

        return f"""
### üí∞ Costo Estimado: ${prediction:,.2f}
**Categor√≠a:** {categoria}

**Factores:**
- Edad: {age} a√±os
- IMC: {bmi:.1f} {'‚ö†Ô∏è' if bmi > 30 else '‚úì'}
- Fumador: {'S√≠ ‚ö†Ô∏è' if smoker == 'S√≠' else 'No ‚úì'}
- Hijos: {children}
"""
    except Exception as e:
        return f"‚ùå Error: {str(e)}"


def predict_diabetes(pregnancies, glucose, blood_pressure, skin_thickness,
                    insulin, bmi, dpf, age):
    try:
        input_data = np.array([[pregnancies, glucose, blood_pressure, skin_thickness,
                               insulin, bmi, dpf, age]])

        input_scaled = scaler.transform(input_data)
        prediction_proba = diabetes_model_final.predict_proba(input_scaled)[0][1]
        prediction = prediction_proba >= best_threshold

        if prediction_proba < 0.3:
            riesgo = "üü¢ Bajo"
        elif prediction_proba < 0.6:
            riesgo = "üü° Moderado"
        else:
            riesgo = "üî¥ Alto"

        return f"""
### {'üî¥ POSITIVO - Riesgo de Diabetes' if prediction else '‚úÖ NEGATIVO'}

**Probabilidad:** {prediction_proba*100:.1f}%
**Riesgo:** {riesgo}
**Umbral:** {best_threshold:.2f}

**Valores:**
- Glucosa: {glucose} {'‚ö†Ô∏è' if glucose > 140 else '‚úì'}
- IMC: {bmi:.1f} {'‚ö†Ô∏è' if bmi > 30 else '‚úì'}
- Edad: {age} a√±os

{'‚ö†Ô∏è Consulte a un m√©dico' if prediction else '‚úì Mantener h√°bitos saludables'}
"""
    except Exception as e:
        return f"‚ùå Error: {str(e)}"


